// addition operator
#include <stdio.h>

int main(void) {

    int a = 5;

    int b = 3;

    int sum = a + b;

    printf("Sum: %i\n", sum); // Output: Sum: 8
}

// subtraction operator
#include <stdio.h>

int main(void) {

    int a = 10;

    int b = 5;

    int difference = a - b;

    printf("Difference: %i\n", difference); // Output: Difference: 5
}

// multiplication operator
#include <stdio.h>

int main(void) {

    int a = 4;

    int b = 3;

    int product = a * b;

    printf("Product: %i\n", product); // Output: Product: 12
}

// division operator
#include <stdio.h>

int main(void) {

    int a = 10;

    int b = 2;

    int quotient = a / b;

    printf("Quotient: %i\n", quotient); // Output: Quotient: 5
}

// modulo operator
#include <stdio.h>

int main(void) {

    int a = 10;

    int b = 3;

    int remainder = a % b;

    printf("Remainder: %i\n", remainder); // Output: Remainder: 1
}
