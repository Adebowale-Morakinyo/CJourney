// equal to(=) operator
#include <stdio.h>

int main(void) {

    int a = 5;

    int b = 5;

    int result = (a == b);

    printf("Result: %i\n", result); // Output: Result: 1
}

// greater than or equal to (>=) operator
#include <stdio.h>

int main(void) {

    int a = 5;

    int  b = 5;

    int result = (a >= b);

    printf("Result: %i\n", result); // Output: Result: 1
}

// less than or equal to (>=) operator
#include <stdio.h>

int main(void) {

    int a = 1;

    int b = 5;

    int result = (a <= b);

    printf("Result: %i\n", result); // Output: Result: 1
}
